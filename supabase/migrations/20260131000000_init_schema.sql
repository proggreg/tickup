-- TickUp Database Schema
-- Restore migration: creates Lists, Todos, and Users tables

-- ===================
-- Lists table
-- ===================
CREATE TABLE IF NOT EXISTS "public"."Lists" (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id uuid REFERENCES auth.users ON DELETE CASCADE,
    name text NOT NULL,
    created_at timestamp with time zone DEFAULT now(),
    updated_at timestamp with time zone DEFAULT now()
);

-- ===================
-- Todos table
-- ===================
CREATE TABLE IF NOT EXISTS "public"."Todos" (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id uuid REFERENCES auth.users ON DELETE CASCADE,
    list_id bigint REFERENCES "public"."Lists" ON DELETE CASCADE,
    name text NOT NULL,
    "desc" text,
    status text,
    priority text,
    type text,
    due_date timestamp with time zone,
    completed_date timestamp with time zone,
    "order" integer,
    color text DEFAULT '#87909e',
    notification_date_time timestamp with time zone,
    notification_sent boolean DEFAULT false,
    links jsonb DEFAULT '[]'::jsonb,
    subtasks jsonb DEFAULT '[]'::jsonb,
    attachments jsonb DEFAULT '[]'::jsonb,
    created_at timestamp with time zone DEFAULT now(),
    updated_at timestamp with time zone DEFAULT now()
);

-- ===================
-- Users table
-- ===================
CREATE TABLE IF NOT EXISTS "public"."Users" (
    id uuid PRIMARY KEY REFERENCES auth.users ON DELETE CASCADE,
    username text,
    push_subscriptions jsonb DEFAULT '[]'::jsonb,
    github_installation_id bigint,
    github_username text
);

-- ===================
-- Indexes
-- ===================
CREATE INDEX IF NOT EXISTS idx_todos_user_id ON "public"."Todos" (user_id);
CREATE INDEX IF NOT EXISTS idx_todos_list_id ON "public"."Todos" (list_id);
CREATE INDEX IF NOT EXISTS idx_todos_notification ON "public"."Todos" (notification_date_time) WHERE notification_sent IS NOT TRUE;
CREATE INDEX IF NOT EXISTS idx_lists_user_id ON "public"."Lists" (user_id);

-- ===================
-- Row Level Security
-- ===================

-- Enable RLS on all tables
ALTER TABLE "public"."Lists" ENABLE ROW LEVEL SECURITY;
ALTER TABLE "public"."Todos" ENABLE ROW LEVEL SECURITY;
ALTER TABLE "public"."Users" ENABLE ROW LEVEL SECURITY;

-- Lists policies: users can only access their own lists
CREATE POLICY "Users can view own lists" ON "public"."Lists"
    FOR SELECT USING (auth.uid() = user_id);

CREATE POLICY "Users can create own lists" ON "public"."Lists"
    FOR INSERT WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Users can update own lists" ON "public"."Lists"
    FOR UPDATE USING (auth.uid() = user_id);

CREATE POLICY "Users can delete own lists" ON "public"."Lists"
    FOR DELETE USING (auth.uid() = user_id);

