-- ===================
-- Auto-set user_id on insert via trigger
-- ===================

-- Function to auto-set user_id from auth context
CREATE OR REPLACE FUNCTION public.set_user_id()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.user_id IS NULL THEN
        NEW.user_id := auth.uid();
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Apply trigger to Lists
CREATE TRIGGER set_lists_user_id
    BEFORE INSERT ON "public"."Lists"
    FOR EACH ROW
    EXECUTE FUNCTION public.set_user_id();

-- Apply trigger to Todos
CREATE TRIGGER set_todos_user_id
    BEFORE INSERT ON "public"."Todos"
    FOR EACH ROW
    EXECUTE FUNCTION public.set_user_id();
